public class Customer {


    int Id;
    String City;

    public Customer() {
        System.out.println("Müşteri nesnesi başlatıldı");

    }

    public String getCity() {
        return City;
    }

    public void setCity(String city) {
        City = city;
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }







}
